// Operadores Aritmeticos: + - * / % **
let n1 = 10;
let n2 = 5;
console.log(n1 + n2);
console.log(n1 - n2);
console.log(n1 * n2);
console.log(n1 / n2);
console.log(n1 % n2);
console.log(2 ** 3);

//Operadores de Atribuição
let n3 = 25;
// n3 = n3 + 2;
n3 += 2; // Esta forma substitui de forma abreviada a linha acima
console.log(n3);

// Incremento e Decremento
let i = 0;
console.log(i++); //Neste caso ele acrescenta 1 na vareavel 
console.log(i--); // Neste caso ele subtrai 1 da vareavel

// Operadores de comparação
// Igualdade de valor - ==
// Igauldade de valor e tipo - ===
// < - > - <= - >= 
// Valores diferente - !=
// Valores e tipos diferentes - !==
console.log(n3 == n2);
